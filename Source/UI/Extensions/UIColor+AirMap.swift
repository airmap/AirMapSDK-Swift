extension UIColor {

	class func airMapLightBlue() -> UIColor {
		return UIColor(red: 136.0/255.0, green: 219.0/255.0, blue: 223.0/255.0, alpha: 1.0)
	}

	class func airMapDarkBlue() -> UIColor {
		return UIColor(red: 52.0/255.0, green: 64.0/255.0, blue: 82.0/255.0, alpha: 1.0)
	}
	
	class func airMapGray() -> UIColor {
		return UIColor(red: 51.0/255.0, green: 63.0/255.0, blue: 72.0/255.0, alpha: 1.0)
	}
	
	class func airMapGreen() -> UIColor {
		return UIColor(red: 126.0/255.0, green: 211.0/255.0, blue: 33.0/255.0, alpha: 1.0)
	}
	
	class func airMapYellow() -> UIColor {
		return UIColor(red: 248.0/255.0, green: 231.0/255.0, blue: 28.0/255.0, alpha: 1.0)
	}
	
	class func airMapRed() -> UIColor {
		return UIColor(red: 163.0/255.0, green: 37.0/255.0, blue: 37.0/255.0, alpha: 1.0)
	}
	
	class func airMapBlack() -> UIColor {
		return .blackColor()
	}

}
